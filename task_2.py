# Створіть дві функції, що обчислюють значення певних алгебраїчних виразів.
# На екрані виведіть таблицю значень цих функцій від -5 до 5 з кроком 0.5.


def func1(x):
    return x ** 2


def func2(x):
    return x+5


i = -5
while i <= 5:
    print(i, ' ** 2 = ', func1(i), '  ', end='')
    print(i, ' + 5 = ', func2(i))
    i += 0.5
